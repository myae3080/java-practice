package practice1;

public class Latte extends Coffee {
    //================================================================================================================
    // member variable
    //================================================================================================================
    private int milk;

    //================================================================================================================
    // constructor
    //================================================================================================================
    public Latte(String coffeeBean) {
        super(coffeeBean);
    }

    //================================================================================================================
    // getter or setter
    //================================================================================================================
    public int getMilk() {
        return milk;
    }

    public void setMilk(int milk) {
        this.milk = milk;
    }

    //================================================================================================================
    // method
    //================================================================================================================
    public void stir(int water, int milk) {
        // method의 경우, this를 붙이지 않는 경향.
        setWater(water);
        setMilk(milk);
    }
}
